void Interrupt_Hw_NvicClearPendingIrq(uint32 p_IrqNumber_s32)
{
    NVIC->ICPR[(p_IrqNumber_s32 >> 5UL)] = (uint32)(1UL << (p_IrqNumber_s32 & 0x1FUL));
}

void Interrupt_Hw_NvicSetPendingIrq(uint32 p_IrqNumber_s32)
{
    NVIC->ISPR[(p_IrqNumber_s32 >> 5UL)] = (uint32)(1UL << (p_IrqNumber_s32 & 0x1FUL));
}

boolean Interrupt_Hw_NvicGetPendingIrq(uint32 p_IrqNumber_s32)
{
    boolean l_State_b = FALSE;

    if ((NVIC->ISPR[(p_IrqNumber_s32 >> 5UL)] & (1UL << (p_IrqNumber_s32 & 0x1FUL))) != 0UL)
    {
        l_State_b = TRUE;
    }
    else
    {
        l_State_b = FALSE;
    }

    return l_State_b;
}

void Interrupt_Hw_NvicEnableIrq(uint32 p_IrqNumber_s32)
{
    NVIC->ISER[(p_IrqNumber_s32 >> 5UL)] = (uint32)(1UL << (p_IrqNumber_s32 & 0x1FUL));
}



void Interrupt_Hw_NvicDisableIrq(uint32 p_IrqNumber_s32)
{
    NVIC->ICER[(p_IrqNumber_s32 >> 5UL)] = (uint32)(1UL << (p_IrqNumber_s32 & 0x1FUL));
}



