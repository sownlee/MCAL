void Interrupt_Hw_NvicSetIrqPriority(sint32 p_IrqNumber_s32, uint32 p_IrqPriority_u32)
{

    uint8 Shift = (uint8) (8U - NVIC_PRIO_BITS);
    if ((sint32)(p_IrqNumber_s32) >= 0)
    {
        NVIC->IP[((uint32)p_IrqNumber_s32)] = (uint8)((p_IrqPriority_u32 << Shift) & (uint32)0xFFUL);
    }
}

