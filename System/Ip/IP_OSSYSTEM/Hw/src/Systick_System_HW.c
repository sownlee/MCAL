/*
* @brief Initialize SysTick timer   
*/
void SysTick_Hw_Init(void)
{
    SysTick->CSRr = SysTick_CSR_ENABLE(0u);
    SysTick->RVR = SysTick_RVR_RELOAD(SYSTICK_MAX);
    SysTick->CVR = SysTick_CVR_CURRENT(0U);
    SysTick->CSRr = SysTick_CSR_ENABLE(1u) | SysTick_CSR_TICKINT(1u) | SysTick_CSR_CLKSOURCE(1u);
}

/*
* @brief Get counter  
*/
uint32 SysTick_Hw_GetCounter(void)
{
    return SYSTICK_GET_COUNTER();
}


