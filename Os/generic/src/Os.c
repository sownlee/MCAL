StatusType GetCounterValue(CounterType CounterID, TickRefType Value)
{
  static uint32 TickValue = 0u;
  TickValue++;
  *Value =  TickValue;
  return E_OK;
}

StatusType GetElapsedValue (CounterType CounterID, TickRefType Value, TickRefType ElapsedValue)
{
  uint32 f_TickCurrentValue_u32 = (uint32)0UL;
  GetCounterValue(CounterID, (TickRefType)&f_TickCurrentValue_u32);
  *ElapsedValue =  f_TickCurrentValue_u32 - *Value;
  return E_OK;
}

